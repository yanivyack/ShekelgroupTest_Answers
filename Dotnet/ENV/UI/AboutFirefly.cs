    public partial class AboutFirefly : System.Windows.Forms.Form
    {
        public AboutFirefly()
        {
            InitializeComponent();

            //  Initialize the AboutBox to display the product information from the assembly information.
            //  Change assembly information settings for your application through either:
            //  - Project->Properties->Application->Assembly Information
            //  - AssemblyInfo.cs
            this.Text = String.Format("About {0}", AssemblyTitle);
            this.labelProductName.Text = AssemblyProduct;
            this.labelVersion.Text = String.Format("Version {0}", AssemblyVersion);
            this.labelCopyright.Text = AssemblyCopyright;
            this.labelCompanyName.Text = AssemblyCompany;

            loadAssembliesList();
        }



        void loadAssembliesList()
        {
            try
            {
                AssemblyName[] assemblies = Assembly.GetEntryAssembly().GetReferencedAssemblies();
                foreach (AssemblyName reference in assemblies)
                {
                    try
                    {
                        ListViewItem item = new ListViewItem(reference.Name);
                        try
                        {
                            Assembly assembly = Assembly.Load(reference.FullName);
                            item.SubItems.Add(assembly.GetName().Version.ToString());
                            item.SubItems.Add(
                                new System.IO.FileInfo(assembly.Location).LastWriteTime.ToShortDateString());
                            listView1.Items.Add(item);
                        }
                        catch(Exception e)
                        {
                            item.SubItems.Add("Error Loading " + e.Message);
                        }
                    }
                    catch
                    {
                    }
                }
            }
            catch
            {
            }
        }

        #region Assembly Attribute Accessors

        public string AssemblyTitle
        {
            get
            {
                // Get all Title attributes on this assembly
                object[] attributes = Assembly.GetEntryAssembly().GetCustomAttributes(typeof(AssemblyTitleAttribute), false);
                // If there is at least one Title attribute
                if (attributes.Length > 0)
                {
                    // Select the first one
                    AssemblyTitleAttribute titleAttribute = (AssemblyTitleAttribute)attributes[0];
                    // If it is not an empty string, return it
                    if (titleAttribute.Title != "")
                        return titleAttribute.Title;
                }
                // If there was no Title attribute, or if the Title attribute was the empty string, return the .exe name
                return System.IO.Path.GetFileNameWithoutExtension(Assembly.GetEntryAssembly().CodeBase);
            }
        }

        public string AssemblyVersion
        {
            get
            {
                return Assembly.GetEntryAssembly().GetName().Version.ToString();
            }
        }

        public string AssemblyDescription
        {
            get
            {
                // Get all Description attributes on this assembly
                object[] attributes = Assembly.GetEntryAssembly().GetCustomAttributes(typeof(AssemblyDescriptionAttribute), false);
                // If there aren't any Description attributes, return an empty string
                if (attributes.Length == 0)
                    return "";
                // If there is a Description attribute, return its value
                return ((AssemblyDescriptionAttribute)attributes[0]).Description;
            }
        }

